    private void boton_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_registrarActionPerformed
        // TODO add your handling code here:
        
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbempresa", "root","alex");
            Statement st = con.createStatement();
            int empleado, horas;
            String proyecto, id;
            
            empleado = Integer.parseInt(this.comboBox_empleado.getSelectedItem().toString());
            horas = Integer.parseInt(this.texto_horas.getText().toString());
            proyecto = this.comboBox_proyecto.getSelectedItem().toString();
            id = UUID.randomUUID().toString();
            System.out.println(id);
            
            String query = "INSERT INTO em_proyecto (ep_id, ep_horas_semanales, ep_empleado, ep_proyecto)"
                     + " VALUES ('"+id+"','"+horas+"','"+empleado+"','"+proyecto+"')";
            st.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Dato ingresado con exito!");
            this.texto_horas.setText("");
            con.close();
            
            
        }catch(Exception e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        
    }//GEN-LAST:event_boton_registrarActionPerformed

